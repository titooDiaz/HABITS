#¿Qué es __init__.py?
#Convertir un directorio en un paquete Python:
#
#Si un directorio contiene un archivo llamado __init__.py, Python lo tratará como un paquete. Esto permite importar módulos desde ese directorio usando notación de puntos (.), como from paquete import modulo.
#Inicializar un paquete:
#
#Puedes usar __init__.py para ejecutar código de inicialización cuando se importa el paquete. Por ejemplo, configurar variables, importar módulos o registrar extensiones.
#Modularidad y organización:
#
#__init__.py permite dividir tu código en múltiples archivos organizados en subdirectorios, pero tratarlos como un único módulo.